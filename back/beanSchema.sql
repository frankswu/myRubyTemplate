    id bigint auto_increment,	<%
		value.each do |k,v|
		propName = k
		propType = v.split(',')[0]
		if 'String' == propType
		  sqlPropType = 'varchar(255)'
		elsif 'double' == propType
		  sqlPropType = 'double(10,8)'
